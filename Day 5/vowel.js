let b = 'e';
switch(b){
    case 'a':
    case 'e':
    case 'i':
    case 'o':
    case 'u':
    console.log("Vowel");
    break;
    default:
    console.log("Consonant");
    break;
}    


    //Switch case using toLowerCase() method

let c = 'A';
switch(c.toLowerCase()){
    case 'a':
    case 'e':
    case 'i':
    case 'o':
    case 'u':
    console.log("Vowel");
    break;
    default:
    console.log("Consonant");
    break;
}